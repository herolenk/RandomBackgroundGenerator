/*This function for randomly Change Box Color */
function randombackgroundcolorgenerator(boxId) {

    var x = Math.round(Math.random() * 255);
    var y = Math.round(Math.random() * 255);
    var z = Math.round(Math.random() * 255);
        
    document.getElementById(boxId).style.backgroundColor = "rgb(" + x + "," + y +","+ z +")";   
}
/*This function for randomly Change Border Color */
function randombordercolorgenerator(boxId) {

    var x = Math.round(Math.random() * 255);
    var y = Math.round(Math.random() * 255);
    var z = Math.round(Math.random() * 255);
        
    document.getElementById(boxId).style.border = "3px solid rgb(" + x + "," + y +","+ z +")";   
}
/*Apply Random Background Color Generator function onclick the button */
    document.getElementById("firstbtn-clr").onclick = function (){randombackgroundcolorgenerator("firstbox")};
/*Apply Random Border Color Generator function onclick the button */
    document.getElementById("firstbtn-bord").onclick = function (){randombordercolorgenerator("firstbox")};

